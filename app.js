const calculator = {
  add: function (a, b) {
    console.log(a + b);
  },
  minus: function (a, b) {
    console.log(a - b);
  },
  divide: function (a, b) {
    console.log(a / b);
  },
  pow: function (a, b) {
    console.log(a ** b);
  },
};

calculator.add(3, 4);
calculator.minus(3, 4);
calculator.divide(9, 3);
calculator.pow(2, 5);
